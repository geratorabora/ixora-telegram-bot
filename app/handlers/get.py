# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º Router ‚Äî –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –¥–ª—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ —ç—Ç–æ–≥–æ –º–æ–¥—É–ª—è
from aiogram import Router

# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º Message ‚Äî –æ–±—ä–µ–∫—Ç –≤—Ö–æ–¥—è—â–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
from aiogram.types import Message, FSInputFile

# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º Command ‚Äî —Ñ–∏–ª—å—Ç—Ä, –∫–æ—Ç–æ—Ä—ã–π –ª–æ–≤–∏—Ç –∫–æ–º–∞–Ω–¥—ã (–Ω–∞–ø—Ä–∏–º–µ—Ä /get)
from aiogram.filters import Command

# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º Path –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –ø—É—Ç—è–º–∏ –∏ —Ñ–∞–π–ª–∞–º–∏
from pathlib import Path


# –°–æ–∑–¥–∞—ë–º —Ä–æ—É—Ç–µ—Ä –¥–ª—è —ç—Ç–æ–≥–æ –Ω–∞–±–æ—Ä–∞ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤
router = Router()

# –£–∫–∞–∑—ã–≤–∞–µ–º –ø–∞–ø–∫—É, –≥–¥–µ –º—ã —Ö—Ä–∞–Ω–∏–º –æ—Ç—á—ë—Ç—ã
STORAGE_DIR = Path("storage")


@router.message(Command("get"))
async def get_last_report(message: Message):
    """
    –ö–æ–º–∞–Ω–¥–∞ /get:
    - –∏—â–µ—Ç —Ñ–∞–π–ª –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –æ—Ç—á—ë—Ç–∞ –≤ –ø–∞–ø–∫–µ storage
    - –µ—Å–ª–∏ —Ñ–∞–π–ª –Ω–∞–π–¥–µ–Ω ‚Äî –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –µ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é
    """

    # –ò—â–µ–º –ª—é–±—ã–µ —Ñ–∞–π–ª—ã, –∫–æ—Ç–æ—Ä—ã–µ –Ω–∞—á–∏–Ω–∞—é—Ç—Å—è –Ω–∞ last_report –∏ –∏–º–µ—é—Ç –ª—é–±–æ–µ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏–µ
    candidates = list(STORAGE_DIR.glob("last_report.*"))

    # –ï—Å–ª–∏ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ ‚Äî –≥–æ–≤–æ—Ä–∏–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é, —á—Ç–æ –æ—Ç—á—ë—Ç–∞ –ø–æ–∫–∞ –Ω–µ—Ç
    if not candidates:
        await message.answer("–ü–æ–∫–∞ –Ω–µ—Ç –∑–∞–≥—Ä—É–∂–µ–Ω–Ω–æ–≥–æ –æ—Ç—á—ë—Ç–∞. –ü–æ–ø—Ä–æ—Å–∏ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞ –∑–∞–≥—Ä—É–∑–∏—Ç—å —Ñ–∞–π–ª üôÇ")
        return

    # –ë–µ—Ä—ë–º –ø–µ—Ä–≤—ã–π –Ω–∞–π–¥–µ–Ω–Ω—ã–π —Ñ–∞–π–ª (–æ–Ω –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å —Ä–æ–≤–Ω–æ –æ–¥–∏–Ω)
    report_path = candidates[0]

    # –û—Ç–∫—Ä—ã–≤–∞–µ–º —Ñ–∞–π–ª –≤ –±–∏–Ω–∞—Ä–Ω–æ–º —Ä–µ–∂–∏–º–µ (rb) –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –∫–∞–∫ –¥–æ–∫—É–º–µ–Ω—Ç
        # –°–æ–∑–¥–∞—ë–º –æ–±—ä–µ–∫—Ç —Ñ–∞–π–ª–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ, –∫–æ—Ç–æ—Ä—ã–π –ø–æ–Ω–∏–º–∞–µ—Ç aiogram
    input_file = FSInputFile(path=report_path)

    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ñ–∞–π–ª –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é
    await message.answer_document(
        document=input_file,
        caption="–ü–æ—Å–ª–µ–¥–Ω–∏–π –æ—Ç—á—ë—Ç"
    )

